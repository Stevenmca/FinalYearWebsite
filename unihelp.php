      <p align="left"><font face="Arial"><b><a name="I need a letter to confirm my student status, how can I get this?"></a>I need a letter to confirm my student status, how can I get this?</b></font></p>

      <p align="left" class="auto-style6" style="mso-fareast-font-family: Calibri; mso-fareast-theme-font: minor-latin; mso-ansi-language: EN-GB; mso-fareast-language: EN-US; mso-bidi-language: AR-SA">
	  If you require a letter confirming your status as a registered student of 
	  the University of Ulster, or confirming that you have taken leave of 
	  absence or withdrawn from your course, you can print this from the Student 
	  Portal, select Banner, Student Records, Fees and Financial Aid, 
	  Registration, Confirmation of Attendance Letter. This form must be stamped 
	  by the relevant campus Registry Office/Student Service Centre.&nbsp; You must 
	  give at least 24 hours notice that you require the letter to be stamped. 
	  When ready, it may be collected from the office.&nbsp; Any requests for letters 
	  confirming details of awards obtained at the university should be directed 
	  to the Examinations Office at the relevant campus.</p>

